let profession = 'fiscal';

console.log(`Profession: ${profession}`);

switch (profession) {
  case 'fiscal':
    console.log('your shirt is green');
    break;
  case 'bombeiro':
    console.log('your shirt is red');
    break;
  case 'policial':
    console.log('your shirt is blue');
    break;
  default:
    console.log('your shirt is black');
    break;
}
